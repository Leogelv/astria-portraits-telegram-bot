                        try:
                            credits = int(credits_data.strip())
                        except ValueError:
                            logger.error(f"–ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å –æ—Ç–≤–µ—Ç API –≤ —á–∏—Å–ª–æ: {credits_data}")
